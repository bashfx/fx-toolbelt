validate_structure() {
    local base_dir="$1"
    
    print_section "Validating cosmic architecture"
    
    # Check critical paths exist
    local critical_paths=(
        "fx/bash" "fx/rust" "fx/python" "fx/void"
        "thx/external" "thx/temp"
        "ix" "pantheon" "sandbox" "wksp" "vault"
        "pantheon/house" "pantheon/bin"
        "wksp/engine/bin" "wksp/curr"
    )
    
    local missing_count=0
    for path in "${critical_paths[@]}"; do
        if [[ ! -d "$base_dir/$path" ]]; then
            echo -e "${RED}✗ Missing: $path${NC}"
            ((missing_count++))
        fi
    done
    
    if [[ $missing_count -eq 0 ]]; then
        echo -e "${GREEN}${STAR} Architecture validation complete - all etchings drawn${NC}"
        return 0
    else
        echo -e "${RED}✗ Architecture incomplete - $missing_count missing paths${NC}"
        return 1
    fi
}

main() {
    print_header
    
    # Setup SUPER environment variables
    setup_super_env
    
    local target_dir="${1:-$SUPER_HOME}"
    
    echo -e "${CYAN}SUPER_HOME: $SUPER_HOME${NC}"
    echo -e "${CYAN}Target realm: $target_dir${NC}"
    echo
    
    # Remove existing if present (for clean slate)
    if [[ -d "$target_dir" ]]; then
        print_section "Clearing existing void"
        rm -rf "$target_dir"
    fi
    
    # Create the cosmic structure
    create_structure "$target_dir"
    
    # Create manifests and documentation
    create_manifests "$target_dir"
    
    # Validate the architecture
    validate_structure "$target_dir"
    
    if [[ $? -eq 0 ]]; then
        echo
        echo -e "${PURPLE}${COSMOS}═══════════════════════════════════════${COSMOS}${NC}"
        echo -e "${GREEN}${STAR} SUPER REALM MANIFESTED IN COSMIC GLORY ${STAR}${NC}"
        echo -e "${PURPLE}${COSMOS}═══════════════════════════════════════${COSMOS}${NC}"
        echo -e "${CYAN}The void has been parted, the etchings drawn.${NC}"
        echo -e "${CYAN}IX's automation realm awaits systematic excellence.${NC}"
        echo
    else
        echo -e "${RED}✗ Architecture manifestation failed${NC}"
        exit 1
    fi
}

# Execute if run directly
if [[ "${BASH_SOURCE[0]}" == "${0}" ]]; then
    main "$@"
fi